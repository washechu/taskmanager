-- м.023 — отказ от «Отложить до» (snooze) у задач.
--
-- Поле start_date у задач (бывшее «Начало», затем «Отложить до») признано
-- лишним: ради редкого снуза оно висело в форме при создании КАЖДОЙ задачи.
-- Убираем концепцию целиком.
--
-- Колонку tasks.start_date НЕ дропаем (вдруг пригодится, плюс зарезервированный
-- GanttView на неё ссылается), но обнуляем все значения — чтобы:
--   • Гант показывал задачи точками на дедлайне (rangeOf(null, due) = isPoint),
--     единообразно, без «полосок» от старых start_date;
--   • фильтр отложенных в дайджестах (м.022) стал перманентным no-op
--     (start_date is null → условие всегда true).
--
-- Проекты сохраняют свой start_date («начало проекта» для Ганта) — их не трогаем.

update tasks set start_date = null where start_date is not null;
